package chapter10;

public class TurtlePosition {
   private int row =0 ;
   private int column= 0 ;
    public TurtlePosition(int row, int column){
        this.row=row;
        this.column=column;

    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }
}
